<template>
  <div class="edit-wrapper" @click="onItemClick(id)" :class="{ active: active }">
    <slot></slot>
  </div>
</template>
<script lang="ts">
import { defineComponent } from 'vue'

export default defineComponent({
  props: {
    id: {
      type: String,
      required: true,
    },
    active: {
      type: Boolean,
      default: false
    }
  },
  emits: ['setActive'],
  setup(props, context) {
    const onItemClick = (id: string) => {
      context.emit('setActive', id)
    }

    return {
      onItemClick
    }
  },
})
</script>
<style scoped>
  .edit-wrapper {
    padding: '0px';
    cursor: pointer;
    border: 1px solid transparent;
    user-select: none;
  }
  .edit-wrapper:hover {
    border: 1px solid #ccc;
  }
  .edit-wrapper.active {
    border: 1px solid #1890ff;
    user-select: none;
  }
</style>
